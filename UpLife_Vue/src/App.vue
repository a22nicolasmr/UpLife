<script>
import BarraNavegacion from "./components/BarraNavegacion.vue";

export default {
  components: {
    BarraNavegacion,
  },
  computed: {
    rutaActual() {
      return this.$route.path;
    },
    mostrarBarra() {
      return (
        this.$route.path !== "/formularios/rexistro" &&
        this.$route.path !== "/formularios/inicio"
      );
    },
    nombreUsuario() {
      return this.$route.query.nombre;
    },
  },
};
</script>

